function fnGetVisitorService(){

  if($.cookie("WL_PCID_DATA") != undefined){
    var cookiePCID_DATA = JSON.parse($.cookie("WL_PCID_DATA"));

    if(cookiePCID_DATA["pcid"] == $.cookie("WL_PCID")){
      fnGetVisitorServiceLoad(cookiePCID_DATA);
    }else{
      fnGetVisitorServiceAjax();
    }
  }else{
    fnGetVisitorServiceAjax();
  }
}

function fnGetVisitorServiceAjax(){
  $.ajax({
    type    : "POST"
    ,url    : "/visitor/getVisitorService.do"
    ,dataType 	: "json"
    ,success: function(sdata){

      fnGetVisitorServiceLoad(sdata);

      //속도 개선을 위해 쿠키를 담는다.3시간 단위
      var date = new Date();
      date.setTime(date.getTime() + 3*60*60*1000);  //3시간

      $.removeCookie("WL_PCID_DATA");
      $.removeCookie("WL_PCID_DATA", {path:"/", domain:"seoul.go.kr"});

      $.cookie("WL_PCID_DATA", JSON.stringify(sdata), {expires:date, path:"/", domain:"seoul.go.kr"});
      //$.cookie("WL_PCID_DATA", JSON.stringify(sdata), {expires:date, path:'/'});

    }
    ,error  : function(edata){
      console.log("getVisitorService failed");
    }
  });
}

function fnGetVisitorServiceLoad(sdata){
  if(sdata){
    $("#VS").empty();
    $.each(sdata.recom_list, function(index, data) {
      //console.log("length::"+(data.SERVICE_NAME).length);
      //console.log("substr::"+(data.SERVICE_NAME).substr(0,8));

      var recomList= "";
      var cutSvcNm = "";
      var SERVICE_NAME = data.SERVICE_NAME;
      SERVICE_NAME = SERVICE_NAME.replace("원순씨핫라인","시장핫라인").replace("원순씨에게 바랍니다","시장에게 바랍니다");
      SERVICE_NAME = SERVICE_NAME.replace("서울의 산과 공원","서울의 공원");
      SERVICE_NAME = SERVICE_NAME.replace("서울의산과공원","서울의 공원");
      
      if(data.FLAG == "1") {
        recomList+='<li  class="best" title="'+SERVICE_NAME+'" onclick="action_logging({tr_code:\'bookmark\'});">';
      } else {
        recomList+='<li title="'+SERVICE_NAME+'" onclick="action_logging({tr_code:\'bookmark\'});">';
      }
      if( (SERVICE_NAME).length > 11 ) {
        cutSvcNm = (SERVICE_NAME).substr(0,11)+"...";
      } else {
        cutSvcNm = (SERVICE_NAME);
      }
      recomList+='<a href="'+data.SERVICE_URL+'" target="_blank" onclick="action_logging({tr_code:\'bookmark\'});">'+cutSvcNm+'</a></li>';

      //console.log(recomList);
      $("#VS").append(recomList);
    });
  }
}


